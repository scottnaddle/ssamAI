async function main() {
  const buffer = await markdownToHwpx(md, {
    title: "광반응 활동지",
    creator: "ssamAI 샘플 v2",
  });
  const outDir = path.join(__dirname, "slides", "output");
  mkdirSync(outDir, { recursive: true });
  const outPath = path.join(outDir, "광반응_활동지_v2.hwpx");
  writeFileSync(outPath, buffer);
  console.log(`✓ HWPX v2 생성 완료: ${outPath} (${buffer.length} bytes)`);
}

main().catch((e) => {
  console.error("오류:", e.message);
  process.exit(1);
});